alp = {'A':['000000','000001','000010','000100','001000','010000','100000'],
       'B':['001111','001110','001101','001011','000111','011111','101111'],
       'C':['010011','010010','010001','010111','011011','000011','110011'],
       'D':['011100','011101','011110','011000','010100','001100','111100'],
       'E':['100110','100111','100100','100010','101110','110110','000110'],
       'F':['101001','101000','101011','101101','100001','111001','001001'],
       'G':['110101','110100','110111','110001','111101','100101','010101'],
       'H':['111010','111011','111000','111110','110010','101010','011010']}
n = int(input())
nlist = input()
nlist_cp = nlist
al_list = []

for q in range(n):
  for i in range(8):
    # print('q:', q, 'i:', i, 'nlist[:6]:', nlist[:6], 'nlist', nlist, 'al_list:', al_list)
    if nlist[:6] in alp[chr(65+i)]:
      al_list.append(chr(65+i))
      nlist = nlist[6:]
      break

if len(al_list) == len(nlist_cp)//6:
  for i in al_list:
    print(i, end='')
else:
  print(len(al_list)+1)
